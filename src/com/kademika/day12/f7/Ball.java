package com.kademika.day12.f7;

import java.awt.*;

public class Ball {
    public int x;
    public int y;
    public int radius;
    public Color color;

    public Ball(int x, int y, int radius, Color color) {
        this.x = x;
        this.y = y;
        this.radius = radius;
        this.color = color;
    }
}
